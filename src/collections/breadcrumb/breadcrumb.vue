<template>
  <div :class="classes">
    <slot />
  </div>
</template>

<script>
import u from '../../lib/util';
// import Enum from '../../lib/enum';
import Mixins from '../../lib/mixins';

export default {
  name: 'FuiBreadcrumb',
  mixins: [
    Mixins.PSize
  ],
  props: {
    inverted: {
      type: Boolean,
      description: '', // TODO: descr
    },
  },
  events: {
    click: {
      description: 'Click event',
    },
  },
  computed: {
    classes: function () {
      return u.concatClasses(
        'ui',
        ...this.getClassesSize,
        this.inverted && 'inverted',
        'breadcrumb'
      );
    },
  },
};
</script>
